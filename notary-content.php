I run a few <a href="http://perspectives-project.org/notary-servers/">Perspectives Notary Servers</a>. They are free for everyone to use.
<br /><br />
<a href="http://perspectives-project.org/">What is Perspectives?</a> -- Perspectives is a browser extension that verifies to see if other servers on the Internet are seeing the same SSL certificate you are being presented with when visiting an HTTPS site.
<br /><br />
Extensions available: <a href="https://addons.mozilla.org/en-US/firefox/addon/perspectives/">Perspectives (Firefox)</a> and <a href="https://chrome.google.com/webstore/detail/lnppfgdnjafeikakadfopejdpglpiahn">Perspectives (Chrome)</a>
